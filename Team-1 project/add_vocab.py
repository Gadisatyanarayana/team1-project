def load_csv(path):
    with open(path, encoding="utf-8") as f:
        reader = csv.reader(f)
        rows = list(reader)
    return rows

def save_csv(path, rows):
    with open(path, "w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerows(rows)

def main():
    rows = load_csv(CSV_PATH)
    # Build set of existing Hindi keys (normalised)
    existing = set()
    for row in rows:
        if row:
            existing.add(row[0].strip().lower())

    added = 0
    skipped = 0
    new_rows = []
    for hindi, santali in NEW_PAIRS:
        key = hindi.strip().lower()
        if key in existing:
            skipped += 1
        else:
            new_rows.append([hindi.strip(), santali.strip()])
            existing.add(key)
            added += 1

    # Append new rows
    all_rows = rows + new_rows
    save_csv(CSV_PATH, all_rows)

    print(f"Done! Added: {added}  |  Skipped (already existed): {skipped}")
    print(f"Total rows now: {len(all_rows)}")

if __name__ == "__main__":
    main()
